    private void btnViewReportActionPerformed(java.awt.event.ActionEvent evt) {                                              
        int selectedRow = tblCourses.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a course offering from the table.", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Use the row index to get the full CourseOffer object from our list
        CourseOffer selectedOffer = facultyCourseOfferings.get(selectedRow);

        ViewPerformanceReportJPanel reportPanel = new ViewPerformanceReportJPanel(CardSequencePanel, selectedOffer);
        CardSequencePanel.add("ViewPerformanceReportJPanel", reportPanel);
        ((java.awt.CardLayout) CardSequencePanel.getLayout()).show(CardSequencePanel, "ViewPerformanceReportJPanel");
    }                                             


