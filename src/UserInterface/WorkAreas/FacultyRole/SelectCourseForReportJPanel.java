    private void populateTaughtCoursesTable() {
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        model.setRowCount(0);
        facultyCourseOfferings.clear();

        String facultyPersonId = facultyUserAccount.getPersonId();

        // Search through all departments, schedules, and offerings to find matches
        for (Department dept : business.getDepartmentList().getDepartments()) {
            for (CourseSchedule schedule : dept.getMastercoursecatalog().values()) {
                for (CourseOffer offer : schedule.getCourseOfferList()) {
                    if (offer.getFacultyProfile() != null && offer.getFacultyProfile().getPerson().getPersonId().equals(facultyPersonId)) {
                        
                        Object[] row = new Object[3];
                        row[0] = offer.getCourseNumber();
                        row[1] = offer.getSubjectCourse().getName();
                        row[2] = schedule.getSemester(); 
                        model.addRow(row);
                        
                        facultyCourseOfferings.add(offer);
                    }
                }
            }
        }
    }

