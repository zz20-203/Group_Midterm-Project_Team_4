    private void populateTable(String key) {
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) UserAccountTable.getModel();
        model.setRowCount(0);

        UserAccountDirectory uad = business.getUserAccountDirectory();

        for (UserAccount ua : uad.getUserAccountList()) {
            if (ua.getAssociatedPersonProfile() instanceof StudentProfile) {
                StudentProfile sp = (StudentProfile) ua.getAssociatedPersonProfile();

                if (key == null || key.isEmpty() || sp.getPerson().getPersonId().equalsIgnoreCase(key)) {
                    Object[] row = new Object[4];
                    row[0] = sp.getPerson().getPersonId();
                    row[1] = sp.getPerson().getName();
                    row[2] = ua.getUserLoginName();
                    row[3] = ua.getPassword();
                    model.addRow(row);
                }
            }
        }
    }



