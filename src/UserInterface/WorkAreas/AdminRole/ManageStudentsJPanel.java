    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = UserAccountTable.getSelectedRow();

        if (selectedRow < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

   
        String username = (String) UserAccountTable.getValueAt(selectedRow, 2);

        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete the student account: " + username + "?",
                "Confirm Delete",
                javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }


        java.util.Iterator<UserAccount> iterator = business.getUserAccountDirectory().getUserAccountList().iterator();

        while (iterator.hasNext()) {
            UserAccount ua = iterator.next();
            if (ua.getUserLoginName().equals(username)
                    && ua.getAssociatedPersonProfile() instanceof StudentProfile) {


                StudentProfile sp = (StudentProfile) ua.getAssociatedPersonProfile();
                business.getStudentDirectory().getStudentlist().remove(sp);

                iterator.remove();
                javax.swing.JOptionPane.showMessageDialog(this, 
                        "Student account '" + username + "' deleted successfully!");
                break;
            }
        }


        populateTable("");
    
    }//GEN-LAST:event_btnDeleteActionPerformed

